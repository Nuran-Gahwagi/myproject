$(document).ready(function() {


$("#indi").click(function(){
	$(".imgAme").hide()
	$(".imgindi").show()
	$(".imgDub").hide()
});
 $("#amer").click(function(){
	$(".imgindi").hide()
	$(".imgAme").show()
	$(".imgDub").hide()
});
 $("#Dub").click(function(){
	$(".imgindi").hide()
	$(".imgAme").hide()
	$(".imgDub").show()
});
$("#tog").click(function(){
	$(".imgindi").show()
	$(".imgAme").show()
	$(".imgDub").show()
})







































});